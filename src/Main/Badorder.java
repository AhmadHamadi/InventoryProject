    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
         String reno=cmbreitem.getSelectedItem().toString();
      String itname=txtitemname.getText();
      String qua=txtquantity.getText();
      String totp=txttotprice.getText();
        try {
           String sq="INSERT INTO returnStock(receive_number, item_name, quantity, total_price, mark) VALUES ('"+reno+"','"+itname+"','"+qua+"','"+totp+"',1)";
           pst=conn.prepareStatement(sq);
           pst.execute();
           JOptionPane.showMessageDialog(rootPane, "Successfully insert");
           badstockLord();
        } catch (Exception e) {
           JOptionPane.showMessageDialog(rootPane, e);
        }
      stockupdate();
      txtitemname.setText("");
      txtquantity.setText("");
      txttotprice.setText("");
      cmbreitem.removeAllItems();
      lordItemNumber();
    }//GEN-LAST:event_btnAddMouseClicked

