    private void delete()
    {
        int x=JOptionPane.showConfirmDialog(rootPane, "Do you realy want to delete");
        if(x==0)
        {
        try {
                String sql="DELETE FROM returnStock where receive_number='"+ cmbreitem.getSelectedItem().toString() +"'";
                pst=conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(rootPane, "successfully deleted");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane,e);
            }
        badstockLord();
        }
    }
    
    private void stockupdate()
    {
        String totvalue=txttotprice.getText();
        try {
            String squpdate = "UPDATE receivestock SET quentity=quentity -'"+txtquantity.getText()+"', total_price=total_price-'"+totvalue+"' WHERE receive_no='"+cmbreitem.getSelectedItem().toString()+"'";
            pst=conn.prepareStatement(squpdate);
            pst.execute();  
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane,e);              
            }
    }
      
