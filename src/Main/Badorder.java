    private void badstockLord()
    {
        try {
            String sql="SELECT `receive_number` as 'RECEIVE NO', `item_name` as 'ITEM NAME', `quantity` as 'QUANTITY', `total_price` as 'TOTAL PRICE' FROM `returnStock` WHERE mark=1";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            tblbadstock.setModel(net.proteanit.sql.DbUtils.resultSetToTableModel(rs));
            theader();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
    
    private void lordItemNumber()
    {
        try {
            String sql="SELECT receive_no from receivestock where mark=1";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            while(rs.next())
             {
             String name=rs.getString("receive_no");
             cmbreitem.addItem(name);
             }
        } catch (Exception e) {
        }
    }
   
    private void cmbreitemPopupMenuWillBecomeInvisible(javax.swing.event.PopupMenuEvent evt) {//GEN-FIRST:event_cmbreitemPopupMenuWillBecomeInvisible
       
        if(cmbreitem.getSelectedIndex()>0)
        {
            try {
            String sql="SELECT item_name,quentity,buying_price from receivestock where receive_no='"+cmbreitem.getSelectedItem().toString()+"'";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
               while(rs.next())
                {
                    String name=rs.getString("item_name");
                    txtitemname.setText(name);
                    quantity=rs.getString("quentity");
                    buyprice=rs.getString("buying_price");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e);
                }
            txtquantity.setEnabled(true);
        }
    }//GEN-LAST:event_cmbreitemPopupMenuWillBecomeInvisible

